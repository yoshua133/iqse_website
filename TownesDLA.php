<div2><img src="people/images/ctownes.jpg"  width="300" alt="" title=""> <br>
<b><font color="#FF0000"><font size="+2">Prof. Charles H. Townes</font></font></b><br></div2>

The Charles Hard Townes Distinguished Lecturer Award is given each year in honor of Charles H. Townes (July 28, 1915 - January 27, 2015) a Nobel Prize winning American Physicist who greatly enriched physics, his family, and his friends.<br><br>

Specifically he received the Nobel Prize in Physics in 1964 "for fundamental work in the field of quantum electronics, which has led to the construction of oscillators and amplifiers based on the maser-laser principle".  Dr. Townes is known worldwide for his work on the theory and application of the maser, on which he got the fundamental patent, and other work in quantum electronics connected with both maser and laser devices.  More recently, he was the driving force behind the construction of the Infrared Spatial Interferometer (ISI) at Berkeley. <br><br>

In addition to his sustained research activities, Dr. Townes served on numerous scientific committees advising governmental agencies and was active in many professional societies, including, e.g., the Science Advisory Committee to the President of the United States, the Advisory Committee for the first human landing on the moon, and the Defense Department's Committee on the MX missile. <br><br>

With respect to Texas A&M University (TAMU), Dr. Townes has a long history of making contributions the Institute for Quantum Science and Engineering (IQSE), the College of Science (COS), and TAMU. For example, he was the inaugural winner of the Trotter prize (with Dr. Francis Crick) in 2002 and   served faithfully on the TAMU Trotter Prize Steering Committee from 2002 forward. In addition, he participated in several Wyoming Summer Schools, gave  numerous distinguished talks at TAMU including one during the dedication activities of the new TAMU Physics Building, and  donated many of his books to the IQSE Reading Room which bears his name (see <a href="http://iqse.tamu.edu/TownesRR.php" target="_blank">http://iqse.tamu.edu/TownesRR.php</a>).  His dedication to science and contributions to the IQSE, the COS, and TAMU are immeasurable. For these contributions (and more) he was awarded a Honorary Doctorate of Science by Texas A&M University on May 14, 2011 (see <a href="http://iqse.tamu.edu/receptions/2011/townes/townes.php" target="_blank">http://iqse.tamu.edu/receptions/2011/townes/townes.php</a>). And now we establish the Charles Hard Townes Distinguished Lecturer Award to not only honor the contributions which Dr. Townes has made, but also to recognize others who are making similar contributions. <br><br>


